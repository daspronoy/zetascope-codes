# NUMPY-VECTORIZED REFERENCE (fast, for generating ground truth)
# ============================================================
def vectorized_tbd(frames):
    """
    Vectorized shift-and-stack using NumPy.
    Still brute-force, but runs in minutes rather than hours.
    Used to generate the reference answer.
    """
    n_frames, ny, nx = frames.shape
    velocities = np.arange(V_MIN, V_MAX + V_STEP / 2, V_STEP).astype(np.float32)
    n_vel = len(velocities)
    time_steps = np.arange(n_frames, dtype=np.float32)

    best_stat = -1e30
    best_x0 = best_y0 = 0
    best_vx = best_vy = 0.0

    # Precompute all starting coordinates
    x0_grid = np.arange(nx, dtype=np.float32)
    y0_grid = np.arange(ny, dtype=np.float32)

    t_start = time.time()
    total = n_vel * n_vel
    done = 0

    for vi in range(n_vel):
        vx = velocities[vi]
        for vj in range(n_vel):
            vy = velocities[vj]
            done += 1
            if done % 50 == 0:
                elapsed = time.time() - t_start
                eta = elapsed * (total - done) / done if done > 0 else 0
                print(f"\r  Velocity {done}/{total} "
                      f"({100*done/total:.0f}%)  "
                      f"elapsed {elapsed:.1f}s  ETA {eta:.0f}s",
                      end="", flush=True)

            # For each frame, compute displaced coordinates for ALL (x0, y0)
            # x_coords[t] = x0 + vx*t  → shape (nx,) broadcast over frames
            # y_coords[t] = y0 + vy*t  → shape (ny,) broadcast over frames
            stat_map = np.zeros((ny, nx), dtype=np.float64)
            all_valid = np.ones((ny, nx), dtype=bool)

            for t in range(n_frames):
                px = np.round(x0_grid + vx * t).astype(np.int32)  # (nx,)
                py = np.round(y0_grid + vy * t).astype(np.int32)  # (ny,)

                # Masks for valid coordinates
                valid_x = (px >= 0) & (px < nx)   # (nx,)
                valid_y = (py >= 0) & (py < ny)   # (ny,)

                # 2D validity: outer product
                valid_2d = valid_y[:, None] & valid_x[None, :]  # (ny, nx)
                all_valid &= valid_2d

                # Clamp for safe indexing (invalid pixels get clamped, masked later)
                px_safe = np.clip(px, 0, nx - 1)
                py_safe = np.clip(py, 0, ny - 1)

                # Gather pixel values: frame[t, py_safe[y0], px_safe[x0]]
                stat_map += frames[t][np.ix_(py_safe, px_safe)]

            # Mask out invalid trajectories
            stat_map[~all_valid] = -1e30

            # Find maximum in this velocity slice
            idx = np.argmax(stat_map)
            iy, ix = np.unravel_index(idx, (ny, nx))
            s = stat_map[iy, ix]
            if s > best_stat:
                best_stat = s
                best_x0 = int(ix)
                best_y0 = int(iy)
                best_vx = float(vx)
                best_vy = float(vy)

    elapsed = time.time() - t_start
    print()
    return best_x0, best_y0, best_vx, best_vy, best_stat, elapsed


