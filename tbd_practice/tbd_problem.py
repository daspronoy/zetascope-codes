# ============================================================
# PROBLEM PARAMETERS (Target)
# ============================================================
# NX, NY = 3072, 3072           # Image dimensions (pixels)
# N_FRAMES = 300               # Number of temporal frames
# BG_MEAN = 100.0             # Background Poisson rate (photons/pixel/frame)
# OBJ_SIGNAL = 25.0           # Object signal added per frame (photons)
#                              # Per-frame SNR ~ OBJ_SIGNAL / sqrt(BG_MEAN)
#                              # Stacked SNR  ~ per_frame_SNR * sqrt(N_FRAMES)

# ============================================================
# PROBLEM PARAMETERS (Benchmarking)
# ============================================================
NX, NY = 1024, 1024           # Image dimensions (pixels)
N_FRAMES = 300               # Number of temporal frames
BG_MEAN = 100.0             # Background Poisson rate (photons/pixel/frame)
OBJ_SIGNAL = 35.0           # Object signal added per frame (photons)
                             # Per-frame SNR ~ OBJ_SIGNAL / sqrt(BG_MEAN)
                             # Stacked SNR  ~ per_frame_SNR * sqrt(N_FRAMES)

# Ground truth trajectory — auto-computed from current parameters (see below)

# Velocity search grid
# V_HYP = 155               # Corresponds to 24025 velocity hypothesis
V_HYP = 50
V_MIN  = -3.0               # Min velocity (pixels/frame)
V_MAX  =  3.0               # Max velocity (pixels/frame)
V_STEP = (V_MAX - V_MIN) / (V_HYP - 1)  # Velocity step size
SEED   = 42                 # RNG seed for reproducibility
NUMPY_COMPUTE = False        # Run NumPy vectorized reference search (set False to skip)
VX_VY_RATIO   = 2.0         # Desired ratio of vx / |vy| for the ground truth trajectory


# ============================================================
# GROUND TRUTH (auto-computed to be valid for current params)
# ============================================================
def compute_ground_truth():
    """
    Pick a ground truth trajectory that:
      - keeps the object inside [0, NX-1] x [0, NY-1] for all N_FRAMES
      - uses a fixed 0.25 px/frame grid so values are always clean round numbers,
        independent of V_HYP
    Starting position is centered in the valid range.
    """
    GT_STEP = V_STEP
    velocities = np.arange(V_MIN, V_MAX + GT_STEP / 2, GT_STEP)

    def valid_start_range(v, n_frames, n_pixels):
        """Return (lo, hi) of valid starting indices for velocity v."""
        end_disp = int(round(v * (n_frames - 1)))
        return (0, n_pixels - 1 - end_disp) if v >= 0 else (-end_disp, n_pixels - 1)

    valid_vx = [v for v in velocities
                if valid_start_range(v, N_FRAMES, NX)[0] <= valid_start_range(v, N_FRAMES, NX)[1]]
    valid_vy = [v for v in velocities
                if valid_start_range(v, N_FRAMES, NY)[0] <= valid_start_range(v, N_FRAMES, NY)[1]]

    def pick_v(candidates, positive, target_mag):
        """Pick the valid non-zero velocity whose magnitude is closest to target_mag."""
        nonzero = [v for v in candidates if (v > 0 if positive else v < 0)]
        if not nonzero:
            return 0.0
        return float(min(nonzero, key=lambda v: abs(abs(v) - target_mag)))

    # Choose base magnitude so the trajectory uses ~1/3 of the image in each axis,
    # then scale vx and vy according to VX_VY_RATIO so they are never equal.
    max_vx_mag = max((abs(v) for v in valid_vx if v != 0), default=0.0)
    max_vy_mag = max((abs(v) for v in valid_vy if v != 0), default=0.0)
    base_mag = min(max_vx_mag, max_vy_mag) / 3
    # vx_target / vy_target = VX_VY_RATIO  →  vx_target = ratio * vy_target
    # keep geometric mean equal to base_mag: sqrt(vx_target * vy_target) = base_mag
    vy_target = base_mag / (VX_VY_RATIO ** 0.5)
    vx_target = VX_VY_RATIO * vy_target

    vx = pick_v(valid_vx, positive=True,  target_mag=vx_target)
    vy = pick_v(valid_vy, positive=False, target_mag=vy_target)

    lox, hix = valid_start_range(vx, N_FRAMES, NX)
    loy, hiy = valid_start_range(vy, N_FRAMES, NY)
    x0 = float((lox + hix) // 2)
    y0 = float((loy + hiy) // 2)
    return x0, y0, vx, vy


TRUE_X0, TRUE_Y0, TRUE_VX, TRUE_VY = compute_ground_truth()


# ============================================================
# DATA GENERATION
# ============================================================
def generate_data():
    """Generate synthetic frames with Poisson background + faint moving object."""
    rng = np.random.default_rng(SEED)
    frames = np.zeros((N_FRAMES, NY, NX), dtype=np.float32)

    for t in range(N_FRAMES):
        # Poisson background
        frames[t] = rng.poisson(BG_MEAN, size=(NY, NX)).astype(np.float32)

        # Inject object signal at ground-truth trajectory position
        obj_x = int(round(TRUE_X0 + TRUE_VX * t))
        obj_y = int(round(TRUE_Y0 + TRUE_VY * t))
        if 0 <= obj_x < NX and 0 <= obj_y < NY:
            frames[t, obj_y, obj_x] += rng.poisson(OBJ_SIGNAL)

    return frames


def save_binary(frames, filename="tbd_frames.bin"):
    """Save frames as raw binary for C++ consumption."""
    with open(filename, "wb") as f:
        f.write(struct.pack("iii", NX, NY, N_FRAMES))
        f.write(frames.astype(np.float32).tobytes())
    print(f"  Saved {filename} ({os.path.getsize(filename):,} bytes)")


def save_params(filename="tbd_params.bin"):
    """Save velocity search parameters as raw binary."""
    with open(filename, "wb") as f:
        f.write(struct.pack("fff", V_MIN, V_MAX, V_STEP))
    print(f"  Saved {filename}")


# ============================================================
# BRUTE-FORCE TBD  (Pure Python — the algorithm to port)
# ============================================================
def brute_force_tbd(frames, max_rows=None):
    """
    Brute-force shift-and-stack search.

    For EVERY (x0, y0, vx, vy) combination, compute:
        T = sum_{t} frame[t, round(y0 + vy*t), round(x0 + vx*t)]
    Return the trajectory with the maximum T.

    Parameters
    ----------
    frames : ndarray, shape (N_FRAMES, NY, NX)
    max_rows : int or None
        If set, only search the first max_rows rows of y0 (for quick testing).
    """
    n_frames, ny, nx = frames.shape
    velocities = np.arange(V_MIN, V_MAX + V_STEP / 2, V_STEP).astype(np.float64)
    n_vel = len(velocities)
    search_ny = ny if max_rows is None else min(max_rows, ny)

    total_traj = nx * search_ny * n_vel * n_vel
    print(f"  Image size      : {nx} x {ny}")
    print(f"  Frames          : {n_frames}")
    print(f"  Velocity grid   : {n_vel} x {n_vel} = {n_vel*n_vel} hypotheses")
    print(f"  Search rows     : {search_ny} / {ny}")
    print(f"  Total trajectories : {total_traj:,}")
    print(f"  Total pixel reads  : {total_traj * n_frames:,}")
    print()

    best_stat = -1e30
    best_x0 = best_y0 = 0
    best_vx = best_vy = 0.0

    t_start = time.time()

    for iy0 in range(search_ny):
        if iy0 % 16 == 0:
            elapsed = time.time() - t_start
            pct = 100.0 * iy0 / search_ny
            eta = (elapsed * (search_ny - iy0) / iy0) if iy0 > 0 else 0
            print(f"\r  Row {iy0:4d}/{search_ny} "
                  f"({pct:5.1f}%)  elapsed {elapsed:7.1f}s  ETA {eta:7.0f}s",
                  end="", flush=True)

        for ix0 in range(nx):
            for vi in range(n_vel):
                vx = velocities[vi]
                for vj in range(n_vel):
                    vy = velocities[vj]

                    # --- Shift-and-stack along this trajectory ---
                    stat = 0.0
                    valid = True
                    for t in range(n_frames):
                        px = int(round(ix0 + vx * t))
                        py = int(round(iy0 + vy * t))
                        if px < 0 or px >= nx or py < 0 or py >= ny:
                            valid = False
                            break
                        stat += frames[t, py, px]

                    if valid and stat > best_stat:
                        best_stat = stat
                        best_x0 = ix0
                        best_y0 = iy0
                        best_vx = vx
                        best_vy = vy

    elapsed = time.time() - t_start
    print()
    return best_x0, best_y0, best_vx, best_vy, best_stat, elapsed


