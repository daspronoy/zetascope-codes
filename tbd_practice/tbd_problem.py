"""
Track-Before-Detect (TBD) Brute-Force Algorithm — Interview Problem
=====================================================================

BACKGROUND:
-----------
Track-Before-Detect is used in space domain awareness to find extremely
faint objects moving through a sequence of images. The object is too dim
to detect in any single frame (sub-detection-threshold SNR), but by
coherently integrating pixel values along candidate trajectories across
multiple frames, the signal accumulates as O(N) while noise grows as
O(sqrt(N)), enabling detection.

THE ALGORITHM (Brute-Force Shift-and-Stack):
--------------------------------------------
For each candidate starting position (x0, y0) and velocity (vx, vy):
    1. Predict the object position in each frame t:
           px = round(x0 + vx * t)
           py = round(y0 + vy * t)
    2. Sum the pixel values at those positions across all frames:
           T(x0, y0, vx, vy) = sum_{t=0}^{N-1} frame[t, py, px]
    3. The trajectory with the highest test statistic T is the detection.

Boundary handling: if any predicted position falls outside the image,
that trajectory is invalid and skipped.

YOUR TASK:
----------
1. Port this brute-force algorithm to CUDA/C++.
2. Read the binary data files generated by this script.
3. Produce identical detection results.
4. Report wall-clock time for the search phase.
5. Aim for maximum GPU utilization and memory efficiency.

DELIVERABLES:
    - A CUDA/C++ source file that compiles and runs
    - Detected (x0, y0, vx, vy) matching the reference output
    - Performance measurements (kernel time, total time, speedup)

EVALUATION CRITERIA:
    - Correctness (matching detected trajectory)
    - Speedup over Python baseline
    - Parallelization strategy and memory access patterns
    - Code clarity and engineering quality

DATA FORMAT:
    tbd_frames.bin:
        Header: 3 x int32 (nx, ny, n_frames)
        Data:   n_frames x ny x nx float32 values (row-major, frame-major)
    
    tbd_params.bin:
        3 x float32 (v_min, v_max, v_step)

RUN THIS SCRIPT FIRST to generate the data files, then implement your
CUDA solution to process them.
"""

